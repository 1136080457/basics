package one;

public class emp {

	private Integer id;
	private String name;
	private Integer age;
	public emp() {}
	public emp(Integer id,String name,Integer age) {
		this.age = age;
		this.id = id;
		this.name = name;
	}
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public Integer getAge() {
		return age;
	}
	public void setAge(Integer age) {
		this.age = age;
	}
	
}
